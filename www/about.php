<h2>Unsere Vision</h2>
<h3>Stell dir vor...</h3>

<p>du würdest dich heute entschließen drei fremden Menschen etwas Gutes zu tun. <br>
Sie sind dir dankbar und helfen jeder wiederum drei Menschen ohne eine Gegenleistung zu erwarten. <br>
Es nimmt seinen Lauf und nach <b>nur 12 Tagen</b> wurde jedem Menschen in Hannover geholfen. <br>
Damit nicht genug. Die Botschaft verbreitet sich auf der ganzen Welt <br>
und nach <b> nur 21 Tagen </b>wurde jedem Menschen auf der ganzen Erde etwas gutes getan. <br>
Ganz gleich welcher Religion er angehört und welche poltische Meinung er vertritt </p>
<br>
<h4> Wäre das nicht großartig? </h4>
<br>
<p>Basierend auf dieser Vision entstand das Projekt "Tue Gutes in Hannover" <br>
Wir widmeten uns der Frage wie man es Menschen erleichtert Hilfe zu bekommen <br>
und umgekehrt auch auf vielfältige und einfache Art Hilfe zu leisten <br><br>
Frei nach dem Motto: </p>

<h4> „Sei selbst die Veränderung, die du dir für diese Welt wünscht!“ </h4>

